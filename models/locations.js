const mongoose = require("mongoose");

const locationSchema = new mongoose.Schema(
  {
    cName: {
      type: String,
      required: true,
    },
    cDescription: {
      type: String,
      required: true,
    },
    cImage: {
      type: String,
    },
    cStatus: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const locationModel = mongoose.model("locations", locationSchema);
module.exports = locationModel;
